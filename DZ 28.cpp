#include <iostream>
#include "Tree.hpp"

int main() {
	setlocale(LC_ALL, "Ru");

	// Создаем объект дерева
	Tree tree{ "Яблоки", 10, 15 };

	// Выводим информацию о дереве
	tree.print_tree();

	// Получаем полную стоимость покупки всех плодов
	int total_price = tree.full_price();
	std::cout << "Полная стоимость покупки всех плодов: " << total_price << " руб." << std::endl;

	// Собираем часть плодов и получаем сумму, на которую их можно продать
	int collected_fruits = 5;
	int collected_price = tree.collect(collected_fruits);
	std::cout << "Собрано " << collected_fruits << " плодов на сумму " << collected_price << " руб." << std::endl;

	return 0;
}
